CREATE TABLE IF NOT EXISTS raw_table (
    slug VARCHAR(255) PRIMARY KEY,
    language VARCHAR(10),
    languages JSONB,
    req_id VARCHAR(255),
    title TEXT,
    description TEXT,
    location_name VARCHAR(255),
    street_address TEXT,
    city TEXT,
    state TEXT,
    country VARCHAR(255),
    country_code VARCHAR(10),
    postal_code VARCHAR(20),
    location_type VARCHAR(20),
    latitude FLOAT,
    longitude FLOAT,
    categories JSONB,
    tags JSONB,
    tags5 JSONB,
    tags6 JSONB,
    brand VARCHAR(255),
    promotion_value INTEGER,
    salary_currency VARCHAR(10),
    salary_value INTEGER,
    salary_min_value INTEGER,
    salary_max_value INTEGER,
    benefits JSONB,
    employment_type VARCHAR(20),
    hiring_organization VARCHAR(255),
    source VARCHAR(255),
    apply_url TEXT,
    internal BOOLEAN,
    searchable BOOLEAN,
    applyable BOOLEAN,
    li_easy_applyable BOOLEAN,
    ats_code VARCHAR(255),
    meta_data JSONB,
    update_date TIMESTAMP,
    create_date TIMESTAMP,
    category JSONB,
    full_location TEXT,
    short_location TEXT
);
